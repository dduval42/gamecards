#include <libft.h>

void		ft_putendl(char const *s)
{
	int		i;
	char	c;

	c = '\n';
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, &c, 1);
}
