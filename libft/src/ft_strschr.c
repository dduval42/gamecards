#include <libft.h>

int		ft_strschr(char c, char *spe)
{
	int		j;

	j = 0;
	if (c == '\0')
		return (0);
	while (spe[j])
	{
		if (c == spe[j])
			return(c);
		j++;
	}
	return(-1);
}
