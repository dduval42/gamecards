#include <libft.h>

void	ft_strclr(char *s)
{
	int i;
	int lenght;

	if (s)
	{
		lenght = ft_strlen(s);
		i = 0;
		while (i < lenght)
		{
			s[i] = '\0';
			i++;
		}
	}
}
